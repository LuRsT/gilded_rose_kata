class GildedRose:

    def __init__(self, items):
        self.items = items

    def update_quality_for_items(self):
        for item in self.items:
            strategy = self.get_strategy(item.name)
            strategy(item)

    def get_strategy(self, item_name):
        if item_name == "Aged Brie":
            return self.aged_brie_strategy
        return self.original_strategy

    @staticmethod
    def aged_brie_strategy(item):
        item.sell_in -= 1
        item.quality += 1

        if item.quality > 50:
            item.quality = 50

    @staticmethod
    def original_strategy(item):
        if item.name != "Aged Brie" and item.name != "Backstage passes to a TAFKAL80ETC concert":
            if item.quality > 0:
                if item.name != "Sulfuras, Hand of Ragnaros":
                    item.quality = item.quality - 1
        else:
            if item.quality < 50:
                item.quality = item.quality + 1
                if item.name == "Backstage passes to a TAFKAL80ETC concert":
                    if item.sell_in < 11:
                        if item.quality < 50:
                            item.quality = item.quality + 1
                    if item.sell_in < 6:
                        if item.quality < 50:
                            item.quality = item.quality + 1
        if item.name != "Sulfuras, Hand of Ragnaros":
            item.sell_in = item.sell_in - 1
        if item.sell_in < 0:
            if item.name != "Aged Brie":
                if item.name != "Backstage passes to a TAFKAL80ETC concert":
                    if item.quality > 0:
                        if item.name != "Sulfuras, Hand of Ragnaros":
                            item.quality = item.quality - 1
                else:
                    item.quality = item.quality - item.quality
            else:
                if item.quality < 50:
                    item.quality = item.quality + 1

    # Can't change this
